fn main() {
    generate_default_icons();

    #[cfg(windows)]
    {
        let icon_path = std::path::PathBuf::from(std::env::var_os("OUT_DIR").expect("OUT_DIR is set"))
            .join("openplayer.ico");
        std::fs::write(&icon_path, OPENPLAYER_ICON).expect("generated Windows icon is writable");

        let windows = tauri_build::WindowsAttributes::new().window_icon_path(icon_path);
        let attributes = tauri_build::Attributes::new().windows_attributes(windows);
        tauri_build::try_build(attributes).expect("failed to run Tauri build script");
    }

    #[cfg(not(windows))]
    tauri_build::build();
}

fn generate_default_icons() {
    let icons_dir = std::path::PathBuf::from(
        std::env::var_os("CARGO_MANIFEST_DIR").expect("manifest dir is set"),
    )
    .join("icons");
    std::fs::create_dir_all(&icons_dir).expect("generated icon directory is writable");
    let _ = std::fs::remove_file(icons_dir.join("icon.ico"));
    std::fs::write(icons_dir.join("icon.png"), OPENPLAYER_PNG)
        .expect("generated window icon is writable");
}

